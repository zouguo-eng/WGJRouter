package com.example.bean;

public class MyBaLvDevices {

    private String devName;
    private String devIP;
    private String devMac;
    private String devAccNet;
    private String devAccSha;
    private String devBindMAC;

    public String getDevName() {
        return devName;
    }

    public void setDevName(String devName) {
        this.devName = devName;
    }

    public String getDevIP() {
        return devIP;
    }

    public void setDevIP(String devIP) {
        this.devIP = devIP;
    }

    public String getDevMac() {
        return devMac;
    }

    public void setDevMac(String devMac) {
        this.devMac = devMac;
    }

    public String getDevAccNet() {
        return devAccNet;
    }

    public void setDevAccNet(String devAccNet) {
        this.devAccNet = devAccNet;
    }

    public String getDevAccSha() {
        return devAccSha;
    }

    public void setDevAccSha(String devAccSha) {
        this.devAccSha = devAccSha;
    }

    public String getDevBindMAC() {
        return devBindMAC;
    }

    public void setDevBindMAC(String devBindMAC) {
        this.devBindMAC = devBindMAC;
    }
}
